package com.dolbommon.dbmon.Teacher;

public class ExperienceVO {
	private String exp_no;
	private String userid;
	private String exp_content;
	private String exp_start;
	private String exp_end;
	
	public String getExp_no() {
		return exp_no;
	}
	public void setExp_no(String exp_no) {
		this.exp_no = exp_no;
	}
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public String getExp_content() {
		return exp_content;
	}
	public void setExp_content(String exp_content) {
		this.exp_content = exp_content;
	}
	public String getExp_start() {
		return exp_start;
	}
	public void setExp_start(String exp_start) {
		this.exp_start = exp_start;
	}
	public String getExp_end() {
		return exp_end;
	}
	public void setExp_end(String exp_end) {
		this.exp_end = exp_end;
	}
	
	
}
